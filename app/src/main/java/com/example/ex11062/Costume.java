package com.example.ex11062;

public class Costume {
    private int image;
    private String name;
    private int level;
    private String category;
    private String[] accessories;

    public Costume(int image, String name, int level, String category, String[] accessories) {
        this.image = image;
        this.name = name;
        this.level = level;
        this.category = category;
        this.accessories = accessories;
    }
}
